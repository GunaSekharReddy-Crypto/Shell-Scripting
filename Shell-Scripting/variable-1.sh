#!/bin/bash
# A simple script to demonstrate basic shell commands
# and variable usage in bash.
# Define a variable
GREETING="Hello, World!"
# Print the variable to the console
echo $GREETING
# Define another variable
NAME="Alice"
# Print a personalized greeting
echo "Hello, $NAME! Welcome to the bash scripting tutorial."
# Perform a simple arithmetic operation
NUM1=5
NUM2=10
SUM=$((NUM1 + NUM2))
# Print the result of the arithmetic operation
echo "The sum of $NUM1 and $NUM2 is: $SUM"
# End of script
